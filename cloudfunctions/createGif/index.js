// 云函数入口文件
const cloud = require('wx-server-sdk')

cloud.init({
  env: cloud.DYNAMIC_CURRENT_ENV
})

// 云函数入口函数
exports.main = async (event, context) => {
  try {
    console.log('cloud.DYNAMIC_CURRENT_ENV:', cloud.DYNAMIC_CURRENT_ENV);
    const { frameUrls, duration, frameCount } = event;
    
    if (!frameUrls || !Array.isArray(frameUrls) || frameUrls.length === 0) {
      throw new Error('未提供帧图像URLs');
    }
    
    console.log(`处理帧数据，共${frameUrls.length}帧，总时长${duration}ms`);
    
    // 验证帧URLs
    console.log('帧URLs:', JSON.stringify(frameUrls));
    
    // 不再尝试创建GIF，直接返回帧数据，让前端自己处理动画
    return {
      success: true,
      frameData: {
        frameUrls: frameUrls,
        frameCount: frameCount || frameUrls.length,
        duration: duration || (frameUrls.length * 200) // 默认每帧200ms
      }
    };
  } catch (error) {
    console.error('处理错误:', error);
    return {
      success: false,
      error: error.message
    };
  }
};

